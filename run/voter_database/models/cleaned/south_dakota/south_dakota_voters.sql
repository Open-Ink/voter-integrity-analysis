
  create or replace   view DBT_VOTER_DATA.cleaned.south_dakota_voters
  
   as (
    SELECT '2023-11-06'::date                       as FILE_DATE,
       'SD'                                     as STATE_CODE,
       COUNTY                                   as COUNTY_CODE,
       ID                                       AS VOTER_ID,
       NULL                                     AS PREFIX,
       FIRST_NAME                               AS FIRST_NAME,
       MIDDLE_NAME                              AS MIDDLE_NAME,
       LAST_NAME                                AS LAST_NAME,
       SUFFIX                                   AS NAME_SUFFIX,
       ADDRESS                                  AS RESIDENCE_ADDRESS_LINE_1,
       NULL                                     AS RESIDENCE_ADDRESS_LINE_2,
       CITY                                     AS RESIDENCE_ADDRESS_CITY,
       'SD'                                     AS RESIDENCE_ADDRESS_STATE,
       ZIP                                      AS RESIDENCE_ADDRESS_ZIPCODE,
       'USA'                                    AS RESIDENCE_ADDRESS_COUNTRY,
       NULL                                     AS BIRTH_YEAR,
       NULL                                     AS BIRTH_MONTH,
       NULL                                     AS BIRTH_DATE,
       STATUS                                   as VOTER_STATUS,
       TO_DATE(ORIGINAL_REG_DATE, 'MM/DD/YYYY') AS REGISTRATION_DATE,
       TO_DATE(DATE_LAST_VOTED, 'MM/DD/YYYY')   AS LAST_VOTED_DATE,
       NULL                                     AS LAST_PARTY_VOTED,
       NULL                                     AS CONGRESSIONAL_DISTRICT,
       SENATE                                   AS STATE_SENATE_DISTRICT,
       HOUSE                                    AS STATE_HOUSE_DISTRICT,
       NULL                                     AS JUDICIAL_DISTRICT,
       NULL                                     AS COUNTY_COMMISSION_DISTRICT,
       SCHOOL_BOARD_AREA                        AS SCHOOL_BOARD_DISTRICT,
       NULL                                     AS CITY_COUNCIL_DISTRICT,
       PRECINCT                                 AS COUNTY_PRECINCT,
       NULL                                     AS MUNICIPAL_PRECINCT,
       NULL                                     AS RACE,
       NULL                                     AS GENDER,
       MAILING_ADDRESS                          AS MAILING_ADDRESS_LINE_1,
       NULL                                     AS MAILING_ADDRESS_LINE_2,
       NULL                                     AS MAILING_LINE_3,
       MAILING_CITY                             AS MAILING_CITY,
       MAILING_STATE                            AS MAILING_STATE,
       MAILING_ZIP                              AS MAILING_ZIPCODE,
       MAILING_COUNTRY                          AS MAILING_COUNTRY
FROM RAW.SOUTH_DAKOTA_VOTERS_2023_11_06 Voters
  );

